export function sumar(num1, num2){
    return num1+num2;
}

export function restar(num1, num2){
    return num1-num2;
}

export function multiplicar(num1, num2){
    return num1*num2;
}

export function dividir(num1, num2){
    return num1/num2;
}

export function modulo(num1, num2){
    return num1%num2;
}

export function potencia(num1, num2){
    return num1**num2;
}

export const PI = 3.14159